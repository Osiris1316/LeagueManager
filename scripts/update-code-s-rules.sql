UPDATE season_tier_rules SET rules_full = '## Match Format

All Code S matches are Best of 3 (Bo3). The first player to win 2 games wins the match.

## Scheduling

Players are responsible for scheduling their match with their opponent each round. Rounds last one week. If a match cannot be completed within the round window, notify a league admin to arrange an extension or reschedule.

## Map Draft

Each match begins with a map draft using aoe2cm.net. The map draft link is available on your match page.

Both players ban and pick maps from the season''s map pool using the pre-set provided. The draft determines which maps will be played in the match. The final map chosen after all bans are completed will be played first. Subsequent maps will be chosen by the loser of the previous map, from the available maps in the draft.

If you are unfamiliar with aoe2cm.net, don''t worry — the tool walks you through each step. You and your opponent take turns, and the interface shows you when it''s your turn and what action to take.

The higher ranked player at the time of the match should create the draft using the pre-set, then send the link to your opponent so they can join.

## Match Civ Draft

After the map draft, both players complete a match civ draft using the Bo3 Match Civ Draft link on the match page. This draft determines each player''s available civilization pool for the entire match.

During the match civ draft, players take turns banning and picking civilizations. The result is a set of civilizations each player is allowed to use across all games in the match. Any civilization not in your pool is unavailable to you for the entire series.

As with the map draft, the higher ranked player at the time of the match should create the draft using the pre-set, then send the link to your opponent so they can join.

## Civilization Selection Per Game

Before each game, both players select their civilization for that game using the Civ Draft for Map / Game link on the match page.

**Code S uses single-use civilizations.** You may only use each civilization once per match, regardless of whether you won or lost with it. Once you play a game with a civilization, it is removed from your available pool for the rest of the match.

You may only select from civilizations that are in your pool from the match civ draft and that you have not already used in a previous game.

## Playing the Game

After maps and civilizations are decided for a game, create or join the game lobby in Age of Empires IV. Use the following settings:

1. Public Game
2. Allow Observers: 0 - 2 min. Delay.
3. Win conditions: Landmarks/Sacred/Wonders
4. Starting resources: Standard
5. Starting Age: Age 1
6. Map state: Concealed
7. Map size: Micro (2 players)
8. Biome: Japanese spring (preferred) Unless both players agree to a different biome.
9. Team starting locations: Team Together
10. Seed: No seed
11. Tuning pack: None

## Reporting Results

After each game, notify us of the results in the aoe4-online-league Discord channel, or DM a league admin. The league admin will enter the final results including maps played and civilizations used.

If there is a dispute about a game result, contact a league admin.

## Sportsmanship

- If you experience a disconnect, reach out to your opponent to arrange a regame if the game was still competitive.
- Be respectful in all communication.

## Questions

If anything is unclear, ask in the league Discord. Admins and other players are happy to help.'
